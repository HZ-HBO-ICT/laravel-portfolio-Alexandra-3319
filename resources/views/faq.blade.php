            <h3>Q:How can you print a document from your laptop at HZ? </h3>
            <p>A: First go to <a href="https://print.hz.nl/login.cfm?dest=index.cfm&">print.hz.nl. </a>
                Then log in with your HZ details. When you're logged in upload the file you want to print. Then
                select the printer you want to use and confirm it. Go to the printer that you selected, scan your
                student card and select print. Then follow the instructions on the printer and you're done! </p>
        </post>
